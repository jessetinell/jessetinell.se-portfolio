function getLinks(socialLinks) {
    if (!socialLinks) return []

    let links = []

    socialMedias.forEach(socialMedia => {
        if (socialLinks[socialMedia]) {
            links.push({ url: socialLinks[socialMedia], type: socialMedia })
        }
    })

    return links;

}

